        section = SECTION_NAMES.get(mod_name, f"{mod_name}.rs")
        lines.append(f"# {'-' * 75}")
        lines.append(f"# {section}")
        lines.append(f"# {'-' * 75}")
        lines.append("")
        for func in module_functions[mod_name]:
            lines.append(_format_func_stub(func))
        lines.append("")

